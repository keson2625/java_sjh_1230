package day02;

public class Ex17_For {
	
	public static void main(String[] args) {
		/*"Hello World!"를 5번 출력하는 예제
		 * 반복횟수 : i는 1부터 5까지 1씩증가
		 * 규칙성 : Hllo Wrold! 출력
		 * 반복문 종료 후 : 없음
		 */
		
		int i = 1;
		for(i= 1; i <= 5; ++i) { //for은 보통 ;;(2개)넣고 시작 
			System.out.println("Hello world!");
		}	
	}
}
